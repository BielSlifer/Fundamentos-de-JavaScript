const a = 7
let b = 3

b += a // faz uma atribuição ao valor de b com o valor de a | b = b + a
console.log(b);

b -= 4 // faz uma subtração ao valor de b com o valor de 4 | b = b - 4
console.log(b);

b *= 2 // faz uma multiplicação ao valor de b com o valor de 2 | b = b * 2
console.log(b);

b /= 2 // faz uma divisão ao valor de b com o valor de 2 | b = b / 2
console.log(b);

b %= 2 // faz uma divisão ao valor de b com o valor de 2 | b = b % 2
console.log(b);

b **= 2 // faz uma potenciação ao valor de b com o valor de 2 | b = b ** 2
console.log(b);
